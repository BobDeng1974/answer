                pager.data.length / pager.pageCount;

//                设置当前页码
            if(!isNaN(btn)){//数字按钮
                pager.currentPage=parseInt(btn);
            }else{
                switch(btn){
                    case 'first':
                        pager.currentPage=0;
                        break;
                    case 'prev':
                        if(pager.currentPage>0){
                            --pager.currentPage;
                        }
                        break;
                    case 'next':
                        if(pager.currentPage+1<pager.maxCount){
                            ++pager.currentPage;
                        }
                        break;
                    case 'last':

                        pager.currentPage=pager.maxCount-1;
                        break;
                }
            }
            //创建数字按钮
            //createNumBtn(pager.currentPage);
